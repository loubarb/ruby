weather = 'sunny'

case weather
when 'sunny'
  puts "Wear shorts and a tank top!"
when 'rainy'
  puts "Bring an umbrella."
when 'cloudy'
  puts "Be prepared for possible rain."
when 'foggy'
  puts "Take it slow, visibility is low."
when 'windy'
  puts "Don't wear a dress, it's windy today!"
else
  puts 'Not sure what to do'
end